/*
* Exercicio 8.3.2 - Logica de programacao.
* Utilize a estrutura de controle de repetição enquanto/faça de modo
* a exibir na tela, em ordem decrescente, os números existentes entre 200 e 1,
* inclusive estes.
*/
public class OitavoExercicio {
    public static void main(String[] args) {

        int n = 200;

        while (n >= 1) {
            System.out.println(n);
            n--;
        }
    }
}