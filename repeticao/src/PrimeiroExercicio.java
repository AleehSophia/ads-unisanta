/*
* Exercicio 7.3.1 - Logica de programacao.
* Escreva um algoritmo que, utilizando a estrutura de controle de
* repetição para/fim_para, exiba na tela os números existentes entre 150 e
* 250, inclusive estes.
*/
public class PrimeiroExercicio {
    public static void main(String[] args) {

        int n = 250;

        for (int i = 150; i <= n; i++) {
            System.out.println(i);
        }
    }
}